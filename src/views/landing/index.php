        var filter = /^([7-9][0-9]{9})+$/;
        var result = "" ;
        for(var i = 0; i < res.length; i++) {
            var stripped = res[i];
            if (stripped.value == "") {
                result = false;
            }
            else if (!(filter.test(stripped))) {
                result = false ;
            }
            else if (!(stripped.length == 10)) {
                result = false;
            }
            else result = true ;
        }
        return result;
    }

